package com.company;

public class ABC
{
    public static void printAlphabet()
    {
        char []abc = new char[]{'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z'};
        for (int i = 0; i < abc.length; i++)
        {
            System.out.print(abc[i] + " ");
        }
    }
}
  